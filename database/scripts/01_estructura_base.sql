CREATE TABLE ROL (
    ID_ROL NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    NOMBRE VARCHAR2(50) NOT NULL,
    DESCRIPCION VARCHAR2(250),

    ESTADO CHAR(1)
        DEFAULT 'A'
        NOT NULL,

    FECHA_CREACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    FECHA_ACTUALIZACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    CONSTRAINT PK_ROL
        PRIMARY KEY (ID_ROL),

    CONSTRAINT UQ_ROL_NOMBRE
        UNIQUE (NOMBRE),

    CONSTRAINT CK_ROL_ESTADO
        CHECK (ESTADO IN ('A', 'I'))
);

COMMENT ON TABLE ROL IS
'Roles utilizados para controlar el acceso al sistema SIGEFER';

COMMENT ON COLUMN ROL.ESTADO IS
'A = Activo, I = Inactivo';

