PROMPT 2. TABLA USUARIO
PROMPT ============================================================

CREATE TABLE USUARIO (
    ID_USUARIO NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_ROL NUMBER NOT NULL,

    NOMBRE_USUARIO VARCHAR2(50) NOT NULL,
    NOMBRES VARCHAR2(100) NOT NULL,
    APELLIDOS VARCHAR2(100) NOT NULL,
    CORREO VARCHAR2(150) NOT NULL,

    CLAVE_HASH VARCHAR2(255) NOT NULL,

    ESTADO VARCHAR2(12)
        DEFAULT 'ACTIVO'
        NOT NULL,

    INTENTOS_FALLIDOS NUMBER(3)
        DEFAULT 0
        NOT NULL,

    ULTIMO_ACCESO TIMESTAMP,

    FECHA_CREACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    FECHA_ACTUALIZACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    CONSTRAINT PK_USUARIO
        PRIMARY KEY (ID_USUARIO),

    CONSTRAINT UQ_USUARIO_NOMBRE
        UNIQUE (NOMBRE_USUARIO),

    CONSTRAINT UQ_USUARIO_CORREO
        UNIQUE (CORREO),

    CONSTRAINT FK_USUARIO_ROL
        FOREIGN KEY (ID_ROL)
        REFERENCES ROL (ID_ROL),

    CONSTRAINT CK_USUARIO_ESTADO
        CHECK (
            ESTADO IN (
                'ACTIVO',
                'INACTIVO',
                'BLOQUEADO'
            )
        ),

    CONSTRAINT CK_USUARIO_INTENTOS
        CHECK (INTENTOS_FALLIDOS >= 0)
);

COMMENT ON TABLE USUARIO IS
'Usuarios autorizados para acceder a SIGEFER';

COMMENT ON COLUMN USUARIO.CLAVE_HASH IS
'Hash seguro de la contraseña; nunca debe almacenarse texto plano';

CREATE INDEX IX_USUARIO_ID_ROL
ON USUARIO (ID_ROL);

