PROMPT 5. TABLA INVENTARIO
PROMPT ============================================================

CREATE TABLE INVENTARIO (
    ID_INVENTARIO NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_PRODUCTO NUMBER NOT NULL,

    STOCK_ACTUAL NUMBER(14,3)
        DEFAULT 0
        NOT NULL,

    STOCK_RESERVADO NUMBER(14,3)
        DEFAULT 0
        NOT NULL,

    STOCK_DISPONIBLE NUMBER(14,3)
        GENERATED ALWAYS AS (
            STOCK_ACTUAL - STOCK_RESERVADO
        ) VIRTUAL,

    UBICACION VARCHAR2(100),

    FECHA_ULTIMO_MOVIMIENTO TIMESTAMP,

    FECHA_ACTUALIZACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    CONSTRAINT PK_INVENTARIO
        PRIMARY KEY (ID_INVENTARIO),

    CONSTRAINT UQ_INVENTARIO_PRODUCTO
        UNIQUE (ID_PRODUCTO),

    CONSTRAINT FK_INVENTARIO_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO)
        REFERENCES PRODUCTO (ID_PRODUCTO),

    CONSTRAINT CK_INVENTARIO_STOCK_ACTUAL
        CHECK (STOCK_ACTUAL >= 0),

    CONSTRAINT CK_INVENTARIO_RESERVADO
        CHECK (STOCK_RESERVADO >= 0),

    CONSTRAINT CK_INVENTARIO_DISPONIBLE
        CHECK (STOCK_RESERVADO <= STOCK_ACTUAL)
);

COMMENT ON TABLE INVENTARIO IS
'Existencias actuales y disponibles de cada producto';

COMMENT ON COLUMN INVENTARIO.STOCK_DISPONIBLE IS
'Valor calculado como STOCK_ACTUAL menos STOCK_RESERVADO';

