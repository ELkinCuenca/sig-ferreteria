    APELLIDOS VARCHAR2(100),
    RAZON_SOCIAL VARCHAR2(180),

    TELEFONO VARCHAR2(30),
    CORREO VARCHAR2(150),
    DIRECCION VARCHAR2(300),

    ESTADO CHAR(1)
        DEFAULT 'A'
        NOT NULL,

    FECHA_CREACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    FECHA_ACTUALIZACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    CONSTRAINT PK_CLIENTE
        PRIMARY KEY (ID_CLIENTE),

    CONSTRAINT UQ_CLIENTE_IDENTIFICACION
        UNIQUE (IDENTIFICACION),

    CONSTRAINT CK_CLIENTE_TIPO_IDENTIFICACION
        CHECK (
            TIPO_IDENTIFICACION IN (
                'CEDULA',
                'RUC',
                'PASAPORTE',
                'CONSUMIDOR_FINAL'
            )
        ),

    CONSTRAINT CK_CLIENTE_NOMBRE
        CHECK (
            NOMBRES IS NOT NULL
            OR RAZON_SOCIAL IS NOT NULL
        ),

    CONSTRAINT CK_CLIENTE_ESTADO
        CHECK (ESTADO IN ('A', 'I'))
);

COMMENT ON TABLE CLIENTE IS
'Personas naturales o jurídicas que realizan compras';

