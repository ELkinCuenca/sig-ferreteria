    CONSTRAINT CK_AUDITORIA_OPERACION
        CHECK (
            OPERACION IN (
                'INSERT',
                'UPDATE',
                'DELETE',
                'LOGIN',
                'LOGOUT',
                'ERROR'
            )
        )
);

COMMENT ON TABLE AUDITORIA IS
'Registro de operaciones relevantes realizadas en SIGEFER';

