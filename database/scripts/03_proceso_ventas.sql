PROMPT ============================================================

CREATE OR REPLACE VIEW VW_PRODUCTOS_REPOSICION AS
SELECT
    p.ID_PRODUCTO,
    p.CODIGO,
    p.NOMBRE AS PRODUCTO,
    c.NOMBRE AS CATEGORIA,
    p.UNIDAD_MEDIDA,
    i.STOCK_ACTUAL,
    i.STOCK_RESERVADO,
    i.STOCK_DISPONIBLE,
    p.STOCK_MINIMO,

    CASE
        WHEN i.STOCK_DISPONIBLE = 0
            THEN 'SIN STOCK'
        WHEN i.STOCK_DISPONIBLE <= p.STOCK_MINIMO
            THEN 'STOCK BAJO'
        ELSE 'NORMAL'
    END AS ESTADO_STOCK,

    GREATEST(
        (p.STOCK_MINIMO * 2) - i.STOCK_DISPONIBLE,
        0
    ) AS CANTIDAD_SUGERIDA,

    ROUND(
        GREATEST(
            (p.STOCK_MINIMO * 2) - i.STOCK_DISPONIBLE,
            0
        ) * p.PRECIO_COMPRA,
        2
    ) AS COSTO_REPOSICION_ESTIMADO

FROM PRODUCTO p
INNER JOIN CATEGORIA c
    ON c.ID_CATEGORIA = p.ID_CATEGORIA
INNER JOIN INVENTARIO i
    ON i.ID_PRODUCTO = p.ID_PRODUCTO
WHERE p.ESTADO = 'A'
  AND c.ESTADO = 'A'
  AND i.STOCK_DISPONIBLE <= p.STOCK_MINIMO;

COMMENT ON TABLE VW_PRODUCTOS_REPOSICION IS
'Vista gerencial de productos que requieren reposición';

