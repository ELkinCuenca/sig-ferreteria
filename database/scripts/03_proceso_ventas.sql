CREATE TABLE VENTA (
    ID_VENTA NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_CLIENTE NUMBER NOT NULL,
    ID_USUARIO NUMBER,

    NUMERO_VENTA VARCHAR2(40) NOT NULL,

    FECHA_VENTA TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    SUBTOTAL NUMBER(14,2)
        DEFAULT 0
        NOT NULL,

    DESCUENTO NUMBER(14,2)
        DEFAULT 0
        NOT NULL,

    IMPUESTO NUMBER(14,2)
        DEFAULT 0
        NOT NULL,

    TOTAL NUMBER(14,2)
        DEFAULT 0
        NOT NULL,

    METODO_PAGO VARCHAR2(20)
        DEFAULT 'EFECTIVO'
        NOT NULL,

    ESTADO VARCHAR2(15)
        DEFAULT 'COMPLETADA'
        NOT NULL,

    OBSERVACION VARCHAR2(500),

    FECHA_CREACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    FECHA_ACTUALIZACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    CONSTRAINT PK_VENTA
        PRIMARY KEY (ID_VENTA),

    CONSTRAINT UQ_VENTA_NUMERO
        UNIQUE (NUMERO_VENTA),

    CONSTRAINT FK_VENTA_CLIENTE
        FOREIGN KEY (ID_CLIENTE)
        REFERENCES CLIENTE (ID_CLIENTE),

    CONSTRAINT FK_VENTA_USUARIO
        FOREIGN KEY (ID_USUARIO)
        REFERENCES USUARIO (ID_USUARIO),

    CONSTRAINT CK_VENTA_SUBTOTAL
        CHECK (SUBTOTAL >= 0),

    CONSTRAINT CK_VENTA_DESCUENTO
        CHECK (DESCUENTO >= 0),

    CONSTRAINT CK_VENTA_IMPUESTO
        CHECK (IMPUESTO >= 0),

    CONSTRAINT CK_VENTA_TOTAL
        CHECK (TOTAL >= 0),

    CONSTRAINT CK_VENTA_METODO_PAGO
        CHECK (
            METODO_PAGO IN (
                'EFECTIVO',
                'TARJETA',
                'TRANSFERENCIA',
                'MIXTO'
            )
        ),

    CONSTRAINT CK_VENTA_ESTADO
        CHECK (
            ESTADO IN (
                'PENDIENTE',
                'COMPLETADA',
                'ANULADA'
            )
        )
);

COMMENT ON TABLE VENTA IS
'Cabecera de las transacciones comerciales de venta de SIGEFER';

COMMENT ON COLUMN VENTA.NUMERO_VENTA IS
'Identificador comercial único generado por la API';

CREATE INDEX IX_VENTA_CLIENTE
ON VENTA (ID_CLIENTE);

CREATE INDEX IX_VENTA_USUARIO
ON VENTA (ID_USUARIO);

CREATE INDEX IX_VENTA_FECHA
ON VENTA (FECHA_VENTA);

CREATE INDEX IX_VENTA_ESTADO
ON VENTA (ESTADO);

