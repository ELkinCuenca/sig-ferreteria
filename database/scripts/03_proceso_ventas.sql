CREATE TABLE ALERTA_STOCK (
    ID_ALERTA NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_PRODUCTO NUMBER NOT NULL,
    ID_MOVIMIENTO NUMBER,

    TIPO_ALERTA VARCHAR2(20) NOT NULL,

    STOCK_DETECTADO NUMBER(14,3) NOT NULL,
    STOCK_MINIMO NUMBER(14,3) NOT NULL,

    ESTADO VARCHAR2(15)
        DEFAULT 'PENDIENTE'
        NOT NULL,

    MENSAJE VARCHAR2(500),

    FECHA_GENERACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    FECHA_ATENCION TIMESTAMP,

    CONSTRAINT PK_ALERTA_STOCK
        PRIMARY KEY (ID_ALERTA),

    CONSTRAINT FK_ALERTA_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO)
        REFERENCES PRODUCTO (ID_PRODUCTO),

    CONSTRAINT FK_ALERTA_MOVIMIENTO
        FOREIGN KEY (ID_MOVIMIENTO)
        REFERENCES MOVIMIENTO_INVENTARIO (ID_MOVIMIENTO),

    CONSTRAINT CK_ALERTA_TIPO
        CHECK (
            TIPO_ALERTA IN (
                'STOCK_BAJO',
                'SIN_STOCK'
            )
        ),

    CONSTRAINT CK_ALERTA_ESTADO
        CHECK (
            ESTADO IN (
                'PENDIENTE',
                'ATENDIDA',
                'DESCARTADA'
            )
        ),

    CONSTRAINT CK_ALERTA_STOCK_DETECTADO
        CHECK (STOCK_DETECTADO >= 0),

    CONSTRAINT CK_ALERTA_STOCK_MINIMO
        CHECK (STOCK_MINIMO >= 0),

    CONSTRAINT CK_ALERTA_FECHA
        CHECK (
            ESTADO = 'PENDIENTE'
            OR FECHA_ATENCION IS NOT NULL
        )
);

COMMENT ON TABLE ALERTA_STOCK IS
'Alertas automáticas para apoyar la reposición de productos';

CREATE INDEX IX_ALERTA_PRODUCTO
ON ALERTA_STOCK (ID_PRODUCTO);

CREATE INDEX IX_ALERTA_ESTADO
ON ALERTA_STOCK (ESTADO);

CREATE INDEX IX_ALERTA_FECHA
ON ALERTA_STOCK (FECHA_GENERACION);

