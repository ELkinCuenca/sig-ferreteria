CREATE TABLE DETALLE_VENTA (
    ID_DETALLE_VENTA NUMBER
        GENERATED BY DEFAULT AS IDENTITY,

    ID_VENTA NUMBER NOT NULL,
    ID_PRODUCTO NUMBER NOT NULL,

    CANTIDAD NUMBER(14,3) NOT NULL,
    PRECIO_UNITARIO NUMBER(14,2) NOT NULL,

    DESCUENTO NUMBER(14,2)
        DEFAULT 0
        NOT NULL,

    SUBTOTAL_LINEA NUMBER(14,2)
        GENERATED ALWAYS AS (
            ROUND(
                (CANTIDAD * PRECIO_UNITARIO) - DESCUENTO,
                2
            )
        ) VIRTUAL,

    FECHA_CREACION TIMESTAMP
        DEFAULT CURRENT_TIMESTAMP
        NOT NULL,

    CONSTRAINT PK_DETALLE_VENTA
        PRIMARY KEY (ID_DETALLE_VENTA),

    CONSTRAINT UQ_DETALLE_VENTA_PRODUCTO
        UNIQUE (ID_VENTA, ID_PRODUCTO),

    CONSTRAINT FK_DETALLE_VENTA_VENTA
        FOREIGN KEY (ID_VENTA)
        REFERENCES VENTA (ID_VENTA),

    CONSTRAINT FK_DETALLE_VENTA_PRODUCTO
        FOREIGN KEY (ID_PRODUCTO)
        REFERENCES PRODUCTO (ID_PRODUCTO),

    CONSTRAINT CK_DETALLE_VENTA_CANTIDAD
        CHECK (CANTIDAD > 0),

    CONSTRAINT CK_DETALLE_VENTA_PRECIO
        CHECK (PRECIO_UNITARIO >= 0),

    CONSTRAINT CK_DETALLE_VENTA_DESCUENTO
        CHECK (
            DESCUENTO >= 0
            AND DESCUENTO <= CANTIDAD * PRECIO_UNITARIO
        )
);

COMMENT ON TABLE DETALLE_VENTA IS
'Productos, cantidades y valores que conforman cada venta';

CREATE INDEX IX_DETALLE_VENTA_VENTA
ON DETALLE_VENTA (ID_VENTA);

CREATE INDEX IX_DETALLE_VENTA_PRODUCTO
ON DETALLE_VENTA (ID_PRODUCTO);

